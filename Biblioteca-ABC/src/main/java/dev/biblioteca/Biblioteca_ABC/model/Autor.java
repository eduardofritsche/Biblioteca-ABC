package dev.biblioteca.Biblioteca_ABC.model;



public class Autor {

    private Integer idAutor;
    private String nomeAutor;
    private String cpfAutor;
    private Integer idadeAutor;

    public Integer getIdAutor() {
        return idAutor;
    }

    public void setIdAutor(Integer idAutor) {
        this.idAutor = idAutor;
    }

    public String getNomeAutor() {
        return nomeAutor;
    }

    public void setNomeAutor(String nomeAutor) {
        this.nomeAutor = nomeAutor;
    }

    public String getCpfAutor() {
        return cpfAutor;
    }

    public void setCpfAutor(String cpfAutor) {
        this.cpfAutor = cpfAutor;
    }

    public Integer getIdadeAutor() {
        return idadeAutor;
    }

    public void setIdadeAutor(Integer idadeAutor) {
        this.idadeAutor = idadeAutor;
    }
}
